#if Conditional Statement in Python
#If statement is the simplest form of a conditional statement.
#It executes a block of code if the given condition is true.
#Example:-
age = 20
if age >= 18:
    print("Eligible to vote.")


#Short-Hand if
#Short-hand if statement allows us to write a single-line if statement.
#Example:-
if age >= 18: print("Eligible to vote.")