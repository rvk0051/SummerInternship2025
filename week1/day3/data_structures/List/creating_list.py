# Creating List with Repeated Elements
# We can create a list with repeated elements using the multiplication operator.

# Example:-
twos = [2] * 5
zeroes = [0] * 7
print(twos)  # Output:- [2, 2, 2, 2, 2]
print(zeroes)  # Output:- [0, 0, 0, 0, 0, 0, 0]