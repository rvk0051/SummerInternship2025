# Accessing List Elements
# Elements in a list can be accessed using indexing.
# Python indexes start at 0, so a[0] will access the first element,
# while negative indexing allows us to access elements from the end of the list.
# Like index -1 represents the last elements of list.

# Example:-
a = [10, 20, 30, 40, 50]

# Access first element
print(a[0])    # Output: 10

# Access last element
print(a[-1])   # Output: 50