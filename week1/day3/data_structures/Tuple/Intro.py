# Python Tuples

# A tuple in Python is an immutable ordered collection of elements.
# Tuples are similar to lists, but unlike lists, they cannot be changed after their creation (i.e., they are immutable).
# Tuples can hold elements of different data types.

#Key Characteristics:
# 1.Ordered: Elements have a defined order, and this order will not change. You can access elements by their index.
# 2.Immutable: This is the most crucial characteristic. You cannot add, remove,
# or change elements after the tuple has been created.
# 3.Allows Duplicates: Tuples can contain multiple identical elements.
# 4.Heterogeneous: Can contain elements of different data types (e.g., numbers, strings, lists, even other tuples).
# 5.Fixed Size: Due to immutability, the size of a tuple is fixed once created.