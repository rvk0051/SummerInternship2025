# Built-in Scope

# The built-in scope is the outermost and widest scope in Python.
# It contains all the standard functions and exceptions
# that are automatically available in every Python program — without importing anything.

#These names live in the builtins module.
#You don’t need to import it to use its functions (like print, len, range, etc.).

