'''
find( ) function

The find() function looks for the first occurrence of the provided value.
If the value is not found in that string, it returns -1.
The value can be any character or a substring.

'''
 # Example:
string = 'string manipulation'
index = string.find('a')  # Using find function to find 1st appearance of letter 'a'
print(index)  # Output: 8