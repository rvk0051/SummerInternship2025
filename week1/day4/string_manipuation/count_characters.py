# Count characters

# count() method is used to count a particular character in the string.

# Example:

string = 'In Time Tec'

# count the character 'i'.
count1 = string.count('i')
print("Count of 'i': ", count1) # Output: Count of 'i': 1

# Count spaces.
count2 = string.count(' ')
print("Number of spaces:", count2) # Output: Number of spaces: 2