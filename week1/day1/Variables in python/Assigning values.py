# Variables in Python are assigned values using the = operator.
var1=5 # 5 value is assigned to variable 'var1'
print(var1)

#Python variables are dynamically typed,
# the same variable can hold different types of values during execution.
var1= "Now a string"
print(var1) # Data type of the variable changed dynamically.

#Assigning variables the same value
num1 = num2 = num3 = 100 #All the variables store same value
print(num1, num2, num3)

#Assigning different values to multiple variables simultaneously.
n1, n2, n3= 1, 2.5, "Python"
print(n1, n2, n3)
