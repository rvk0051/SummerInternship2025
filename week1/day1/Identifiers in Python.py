# Identifiers in Python are the names used to identify variables, functions, classes, modules, etc.
# Rules for Identifiers in Python:
# Can contain: letters (a-z, A-Z), digits (0-9), and underscores _
# Cannot start with a digit
# Cannot be a Python keyword (e.g., class, if, for, etc.)
# Case-sensitive: Name and name are different
# Should not use special characters: @, $, %, etc.