# Built-in modules
'''
There are several built-in modules in Python, which you can import whenever you like
'''

# Example:-

import math   # importing 'math' ( a built-in module )

# calling the function 'sqrt' of 'math' module, passing number 4 to it and printing the returned square root value..
# ('sqrt' function returns square root of the number passed to it)
print(math.sqrt(4))

# Output:- 2