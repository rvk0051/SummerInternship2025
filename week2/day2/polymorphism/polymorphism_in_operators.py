'''

Polymorphism in Operators  =>  Operator Overloading:-

In Python, operators like + behave polymorphically,
performing addition, concatenation or merging based on the data type.

'''

# Example:-

print(5 + 10)  # Integer addition
# Output:- 15

print("Hello, " + "World!")  # String concatenation
# Output:- Hello, World!

print([1, 2] + [3, 4])  # List concatenation
# Output:- [1, 2, 3, 4]